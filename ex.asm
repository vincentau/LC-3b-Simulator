		.ORIG	x3000

		LEA R2, NUMBER
		LDW R2, R2, #1
		LEA R1, SIX
		LDW R1, R1, #0
		AND R3, R3, #0

;START INNER LOOP
AGAIN	ADD R3, R3, R2
		ADD R1, R1, #-1
		BRP AGAIN
		LEA R0, STORE
		LDW R0, R0, #0
		STB R3, R0, #1
		STB R3, R0, #0

		HALT

NUMBER	.FILL #2
SIX		.FILL X0006
STORE	.FILL x4000

